'''
Bitwise Operators

| Operator | Meaning     |    |
| -------- | ----------- | -- |
| `&`      | AND         |    |
| `        | `           | OR |
| `^`      | XOR         |    |
| `~`      | NOT         |    |
| `<<`     | Left Shift  |    |
| `>>`     | Right Shift |    |

'''
a = 5   # 101
b = 3   # 011

print(a & b)  # 1
print(a | b)  # 7
print(a ^ b)  # 6

x = 5
print(x << 1)  # 10
print(x >> 1)  # 2

